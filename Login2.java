    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    //Fungsi untuk menampilkan halaman Dashboard jika memasukkan username dan password secara benar maka akan muncul notifikasi "Login Sukses" 
        try{
            con = (Connection) Koneksi.configDB();
            Statement s = con.createStatement();
            String query = "SELECT * FROM login WHERE username='" + inputtxt.getText() +"' and password='" + passtxt.getText() +"'";
            ResultSet rs = s.executeQuery(query);

            if(rs.next()){
                Dashboard2 bor = new Dashboard2();
                bor.setVisible(true);
                this.dispose();
                JOptionPane.showMessageDialog(null, "Login Sukses");

            }else{
                JOptionPane.showMessageDialog(null, "Username atau Password Tidak Sesuai");
            }
        }catch(SQLException ex){
            System.out.println("error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

